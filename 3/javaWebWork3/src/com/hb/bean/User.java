package com.hb.bean;

public class User {
	private String name;
	private String pwd;
	public String getName() {
		return this.name;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	public String getPwd()
	{
		return this.pwd;
	}
	public void setPwd(String pwd)
	{
		this.pwd = pwd;
	}
}
